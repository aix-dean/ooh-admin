export function formatPhoneNumber(phoneNumber: string, countryCode: string): string {
  // Remove all non-digit characters
  const digitsOnly = phoneNumber.replace(/\D/g, "")

  // Find the country format
  const country = countryCodes.find((c) => c.dial_code === countryCode)

  if (!country) {
    return digitsOnly
  }

  // Format based on the country's pattern
  const format = country.format
  let formattedNumber = ""
  let digitIndex = 0

  for (let i = 0; i < format.length; i++) {
    if (format[i] === "X") {
      if (digitIndex < digitsOnly.length) {
        formattedNumber += digitsOnly[digitIndex]
        digitIndex++
      }
    } else {
      if (digitIndex < digitsOnly.length) {
        formattedNumber += format[i]
      }
    }
  }

  return formattedNumber
}

export function getMaxLengthForCountry(countryCode: string): number {
  const country = countryCodes.find((c) => c.dial_code === countryCode)
  if (!country) return 11 // Default max length

  // Count the number of X's in the format string
  return (country.format.match(/X/g) || []).length
}
